<p>In my senior year of high school, I wrote a program to "scout" the opponents of our robotics team,
	so that the team could quickly develop strategies for each match. The program used a simple command-line
	interface written in Java and worked by collecting and aggregating data acquired through an online API.
	It even correctly predicted the team's overall record at the 2018 FRC World Championship! I later went on
	to write a basic interface for the app so that it was more easily usable by other team members.
</p>
<h2 class="SectionHeader">Whirlpool Internship</h2>
<p>The scouting program that I wrote helped land me an internship at Whirlpool right out of high school, where
	I worked for two straight summers. While I was there, I worked on a number of data analysis tools for internal
	use by company quality analysts and engineers. These tools largely consisted of web apps with a backend written
	in Google Apps Script, and a frontend written in either vanilla JavaScript or React.js.
</p>
<p>In my first year, I independently proposed, and subsequently developed, a web app called the "Claim Coding Tool",
	which was intended to help improve the efficiency of data entry by Whirlpool's claim coding team based in India.
	I worked directly with this team to develop the app, which I later presented to multiple company vice presidents.
</p>
<p>My second year consisted of development on existing data analysis tools, as well as the creation of a new tool
	called "Go-And-See". This app allows employees to subscribe to various quality data streams, and to apply custom
	filters on them. They would then receive emails containing the most recent dataset on a user-defined interval.
</p>

