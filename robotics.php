<h2 class="SectionHeader">Swerve Drive</h2>
<p>In my senior year, our team took an offseason to learn about a new omnidirectional drive base, colloquially referred to
	as a "swerve drive". This type of drive base has four wheels - one on each corner - that can each independently pivot
	to any direction. That means eight motors, four encoders (minimum), and a whole ton of programming challenges.
	So, over the summer, I loaded a swerve drive prototype into the trunk of my car, and took it home to experiment with.
	After three days of non-stop researching and programming, I had written a fully-functioning program to control the swerve
	drive with two joysticks on an XBox controller.
</p>
<p>We went on to use a swerve base on our 2018 robot, and the competitive edge propelled us to both the state and world
	championship events for the first time in our team's history. At the world championship event, I was honored to serve
	as the alliance captain (leader and representative) of four different teams through the playoffs, where we were eliminated
	by the eventual world champions.
</p>
