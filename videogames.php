<p>Video games were how I first got into computer programming. I've been toying around with different game engines
	and platforms since middle school. And over the last few years, my brother Alex and I have been participating as a team
	in a number of competitive "game jams". Alex handles the graphics and sounds, while I write the game logic.
	The goal of a game jam is to develop the best video game within a short
	period of time - often only a few days. After development, participants all vote for the winners.
</p>
<h2 class="SectionHeader">Liquid Bacon</h2>
<p>In November 2018, we created "Liquid Bacon" for the GMC Jam. Built to satisfy the theme "Chemical Reactions",
	this game follows a stingy protagonist who would rather try and find the recipe for a mass-produced product than
	simply buy it. The player does so by talking to wacky characters, wandering remote areas, pillaging trash cans,
	fighting off bears, and exploring sketchy warehouses. The game was voted as the winner out of all 26 entries!
</p>
<p>Liquid Bacon was made in GameMaker: Studio and written in GameMaker's proprietary scripting language GML.
	It was built for Windows. You can download and play the game <a href="#">here</a>.
</p>
<h2 class="SectionHeader">The Empty Cone</h2>
<p>In March 2020, we participated in the 46th Ludum Dare, creating "The Empty Cone". The theme was "Keep It Alive",
	so in "The Empty Cone" you must keep your ice cream business alive. That is, both finanically alive from bankruptcy,
	and literally alive from random monster attacks! The game placed in the top 8% out of all entries overall.
</p>
<p>The Empty Cone was made with Godot and written in Godot's scripting language GDScript. It was built for Windows and
	for the web, which means you can play it in your browser <a href="#">here</a>.
</p>
